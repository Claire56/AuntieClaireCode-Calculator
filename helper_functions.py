

import re


def adding(a , b):
#this function sums up the two numbers
    return a + b

def subtraction(a , b):
#this function returns the difference between the two numbers. the position matters
    return a - b

def multiplication(a , b):
#this function returns the product of the two numbers
    return a * b

def modulo(a , b):
 #this function returns the remainder after dividing the irst number by the second number
    return a % b
    

def power_of(a , b):
#this function raises the first number to the second number
    return a ** b
    